// Practice301_01クラス:変数の値を表示
public class Practice301_01 {
	//メインメソッド
	public static void main(String[] args) {

		int num1;//整数代入用変数num1を定義
		int num2;//整数代入用変数num2を定義

		num1 = 1;//num1に1を代入
		num2 = 2;//num2に2を代入

		System.out.println(num1);//num1を表示
		System.out.println(num2);//num2を表示
		System.out.println((num1 + num2));//num1とnum2を加算した値を表示
	}
}

/*実行結果
1
2
3
 */