#include<stdio.h>
int main(void) {
	int kuku[9][9],i, j;
	for (i = 0; i <= 8; i++) {
		for (j = 0; j <= 8; j++) {
			kuku[i][j] = (i + 1) * (j + 1);
			printf("%2d " ,kuku[i][j]);
		}
		printf("\n");
	}
	return 0;
}

