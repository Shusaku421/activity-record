#include<stdio.h>
int main(void) {
	int a[5] = { 3,1,5,6,2 }, b[5], i, j;
	for (i = 0; i <= 4; i++) {
		b[i] = a[5 - (i + 1)];
}
	printf(" a, b\n");
	for (j= 0; j <= 4; j++) {
		printf("%2d,%2d\n",a[j],b[j]);
	}
	return 0;
}

